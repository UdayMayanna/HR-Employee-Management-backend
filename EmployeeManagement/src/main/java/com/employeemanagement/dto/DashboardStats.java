package com.employeemanagement.dto;

public class DashboardStats {
   private long employeeCount;
   private long departmentCount;
   private long managerCount;
   private long teamLeaderCount;
public long getEmployeeCount() {
	return employeeCount;
}
public void setEmployeeCount(long employeeCount) {
	this.employeeCount = employeeCount;
}
public long getDepartmentCount() {
	return departmentCount;
}
public void setDepartmentCount(long departmentCount) {
	this.departmentCount = departmentCount;
}
public long getManagerCount() {
	return managerCount;
}
public void setManagerCount(long managerCount) {
	this.managerCount = managerCount;
}
public long getTeamLeaderCount() {
	return teamLeaderCount;
}
public void setTeamLeaderCount(long teamLeaderCount) {
	this.teamLeaderCount = teamLeaderCount;
}
   
   
}
